public class True_or_False {
  public static void main(String[] args) {
    boolean isItPayDay = false;
    boolean areYouBroke = true;
    boolean isSixBigger = (6 > 5);
    boolean isSevenSmallerOrEqual = (7 <= 4);
    System.out.println("Is it pay day?: " + isItPayDay);
    System.out.println();
    System.out.println("Are your broke?: " + areYouBroke);
    System.out.println();
    System.out.println("Is six is bigger than five?: " + isSixBigger);
    System.out.println();
    System.out.println("Is seven less than or equal to 4?: " + isSevenSmallerOrEqual);
  }
}